#인덱싱과 슬라이싱

str = "You make me happy"
print(str[2])
print(str[15])


# 해당 인덱스부터 해당 인덱까지 슬라이싱
print(str[4: 8])

# 해당 인덱스부터 끝까지 슬라이싱
print(str[-5:]) 
print(str[4:])
# 처음부터 해당 인덱스까지 슬라이싱
print(str[:3])

# 모두 출력 
print(str[:])

