# 연산자
# ** 거듭제곱
# // 소수점을 버림
# % 나머지반환 

print(a-b) # print 하고 나서는 무조건 줄바꿈이 일어남
print(a+b)
print(a*b ,"\n",b/2) # 여러개 출력하고자 할때 , 으로 구분가능

print(a**3)
print(a/3) # 소수점까지 모두출력
